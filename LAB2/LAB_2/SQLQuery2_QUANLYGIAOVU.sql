SELECT HV.MAHV, CONCAT(HV.HO, ' ', HV.TEN) AS HOTEN
FROM HOCVIEN HV
INNER JOIN LOP L ON HV.MALOP = L.MALOP
INNER JOIN KETQUATHI KQ ON HV.MAHV = KQ.MAHV
INNER JOIN MONHOC MH ON MH.MAMH = KQ.MAMH
WHERE (L.MALOP LIKE 'K%' AND MH.MAMH = 'CTRR' )
GROUP BY HV.MAHV,  HV.HO, HV.TEN
HAVING MAX(KQ.DIEM) < 5
